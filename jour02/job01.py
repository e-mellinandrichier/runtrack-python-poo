class Rectangle:
    def __init__(self):
        self.__longueur = 0
        self.__largeur = 0

    def getLongueur(self):
        return self.__longueur

    def getLargeur(self):
        return self.__largeur
    
    def setLongueur(self, longueur):
        self.__longueur = longueur
    
    def setLargeur(self,largeur):
        self.__largeur = largeur


